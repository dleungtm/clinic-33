<template>
  <section class="add-user-view">
  <div class="content">
    <div class="subsection">
    <form style="margin: 15px 15px;">
      <h1> User Information </h1>
      <div style="margin: 10px 0;">
        <span class="user-password">First Name: </span>
        <input type="text" v-model="first_name"></input>
      </div>
      <div style="margin: 10px 0;">
        <span class="user-password">Last Name: </span>
        <input type="text" v-model="last_name"></input>
      </div>
      <div style="margin: 10px 0;">
        <span class="user-username">Username: </span>
        <input type="text" :value="username" v-model="username"></input>
      </div>
      <div style="margin: 10px 0;">
        <span class="user-password">Password: </span>
        <input type="password" v-model="password"></input>
      </div>
      <div style="margin: 10px 0;">
        <span class="user-password">Phone: </span>
        <input type="text" v-model="phone_number"></input>
      </div>
      <div style="margin: 10px 0;">
        <span class="user-password">Address: </span>
        <input type="text" v-model="address"></input>
      </div>
      <hr/>
      <h1>User Health Information</h1>
      <div style="margin: 10px 0;">
        <span class="user-password">DOB: </span>
        <input type="text" v-model="dob"> (YYYY-MM-DD)</input>
      </div>
      <div style="margin: 10px 0;">
        <span class="user-password">PHN: </span>
        <input type="text" v-model="phn"></input>
      </div>
      <div style="margin: 10px 0;">
        <span class="user-password">Height: </span>
        <input type="text" v-model="height">cm</input>
      </div>
      <div style="margin: 10px 0;">
        <span class="user-password">Blood Type: </span>
        <input type="text" v-model="blood_type"></input>
      </div>
      <div style="margin: 10px 0;">
        <span class="user-password">Sex: </span>
        <input type="text" v-model="sex"></input>
      </div>
    </form>
    <button type="button" class="button--default" @click="submitInsert">Add User</button>
    </div>
  </div>
  </section>
</template>

<script>
import axios from '~/plugins/axios'

export default {

  data () {
    return {
      user_id: '',
      first_name: '',
      last_name: '',
      phone_number: '',
      address: '',
      username: '',
      password: '',
      is_active: true,
      dob: '',
      phn: '',
      height: '',
      blood_type: '',
      sex: ''
    }
  },

  methods: {
    submitInsert () {
      let self = this

      axios.post('/api/users/add', {
        headers:
          {
            'Content-Type': 'application/json'
          },
        data:
          {
            first_name: self.first_name,
            last_name: self.last_name,
            phone_number: self.phone_number,
            address: self.address,
            username: self.username,
            password: self.password,
            is_active: self.is_active,
            dob: self.dob,
            phn: self.phn,
            height: self.height,
            blood_type: self.blood_type,
            sex: self.sex
          }})
        .then((res) => {
          // res.data should contain the url for redirecting... bad practice
          self.$nuxt.$router.replace({ path: res.data })
        })
        .catch((e) => {
          console.log(e)
        })
    }
  },

  head () {
    return {
      title: `Add New User`
    }
  }
}
</script>

